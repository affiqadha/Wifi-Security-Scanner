  static Future<void> initialize() async {
    if (_initialized) return;
    
    try {
      print('üîß Initializing MAC Vendor Service...');
      
      // Preload the built-in database
      print('‚úÖ Loaded ${_builtInVendors.length} MAC prefixes from built-in database');
      
      // Try to load additional vendors from online API (optional)
      try {
        // This is optional - app works fine without it
        await _downloadVendorDatabase();
      } catch (e) {
        print('‚ö†Ô∏è  Could not download vendor database: $e');
        print('üì¶ Using built-in database only');
      }
      
      _initialized = true;
      print('‚úÖ MAC Vendor Service initialized successfully');
    } catch (e) {
      print('‚ùå Error initializing MAC Vendor Service: $e');
      _initialized = true; // Set to true anyway so we can use built-in database
    }
  }

  static Future<void> _downloadVendorDatabase() async {
    // Optional: Try to get additional vendors online
    // This can timeout/fail and app will still work
    try {
      final prefs = await SharedPreferences.getInstance();
      final lastUpdate = prefs.getInt('mac_vendor_last_update') ?? 0;
      final now = DateTime.now().millisecondsSinceEpoch;
      
      // Only update if > 30 days old
      if (now - lastUpdate > 30 * 24 * 60 * 60 * 1000) {
        print('üì• Attempting to download MAC vendor database...');
        // Download logic here (optional)
        await prefs.setInt('mac_vendor_last_update', now);
      }
    } catch (e) {
      // Fail silently - built-in database is enough
      print('‚ÑπÔ∏è  Vendor database download skipped: $e');
    }
  }

  static Future<String> lookupVendor(String macAddress) async {
    if (!_initialized) {
      await initialize();
    }
    
    try {
      // Normalize MAC address
      final mac = macAddress.toUpperCase().replaceAll('-', ':');
      
      // Check cache first
      if (_cachedVendors.containsKey(mac)) {
        return _cachedVendors[mac]!;
      }
      
      // Extract first 8 characters (first 3 octets)
      final prefix = mac.substring(0, 8);
      
      // Check built-in database
      if (_builtInVendors.containsKey(prefix)) {
        final vendor = _builtInVendors[prefix]!;
        _cachedVendors[mac] = vendor;
        return vendor;
      }
      
      // Check for locally administered addresses
      final firstOctet = mac.substring(0, 2);
      final firstByte = int.parse(firstOctet, radix: 16);
      if ((firstByte & 0x02) != 0) {
        return 'Locally Administered';
      }
      
      // Try online lookup (with short timeout)
      try {
        final response = await http.get(
          Uri.parse('https://api.macvendors.com/$mac'),
        ).timeout(Duration(seconds: 2));
        
        if (response.statusCode == 200) {
          final vendor = response.body.trim();
          if (vendor.isNotEmpty && vendor != 'Not Found') {
            _cachedVendors[mac] = vendor;
            // Optionally save to built-in database for next time
            return vendor;
          }
        }
      } catch (e) {
        // Online lookup failed, continue to unknown
      }
      
      // Unknown vendor
      return 'Unknown';
    } catch (e) {
      print('‚ùå Error looking up vendor for $macAddress: $e');
      return 'Unknown';
    }
  }

